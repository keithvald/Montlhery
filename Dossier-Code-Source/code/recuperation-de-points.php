      <h2 class="text-left font-weight-bold pb-4 mt-1 title-cases-law">
        Cas n°1 : Stage volontaire – récupération de points
      </h2>
    </div>
  </div>
  <div class="row">
    <div class="col-12 col-md-6">
      <p class="font-weight-bold">Contexte</p>
      <p>
        Vous avez été verbalisé et avez reçu du Ministère de l’intérieur un
        courrier vous informant de la perte de vos points. Ce document porte la
        référence 48.
      </p>
      <p class="font-weight-bold">Introduction</p>
      <p>
        Ce stage vous permettra de récupérer
        <strong class="c-blue">4 points</strong> sans pouvoir dépasser un total,
        à l’issue du stage, de 12 points (6 en période probatoire). Ces points
        sont inscrits au fichier national du permis de conduire en retenant
        comme date de valeur le lendemain du deuxième jour du stage.
      </p>
    </div>
    <div class="col-12 col-md-6">
      <p class="font-weight-bold">Conditions</p>
      <p>
        Il existe certaines conditions pour pouvoir récupérer des points à la
        suite de ce stage:
      </p>
      <ul class="ul-style">
        <li>Disposer au minimum d’un point sur votre permis de conduire.</li>
        <li>
          Ne pas avoir récupéré de points à la suite d’un stage analogue s’étant
          déroulé moins de 1 ans avant le stage.
        </li>
        <li>A voir effectivement perdu des points à la date du stage.</li>
      </ul>
    </div>
  </div>
</div>
<div class="row p-8 bg-custom-grey">
  <div class="col-12">
    <p class="text-center">
      <img
        src="../assets/img/illustrations/rules.png"
        alt="picture"
        class="pictures-points"
      />
    </p>
    <p class="font-weight-bold">Perte de points</p>
    <p>
      Il existe des <strong class="c-blue">délais</strong> entre la perte de
      points et son imputation sur le fichier national puis le courrier en
      informant le conducteur. Ainsi un conducteur peut avoir perdu
      juridiquement l’ensemble de ses points sans que cela ne soit encore
      inscrit sur le fichier national. C’est le
      <strong class="c-blue">capital points</strong>, tel qu’il figure au
      <strong class="c-blue">fichier national des permis de conduire</strong>,
      qui fait foi. Avant de s’inscrire à un stage, si vous n’avez pas reçu de
      document référence 48, vous êtes invité à vous rendre dans la
      <strong class="c-blue">préfecture</strong> ou la
      <strong class="c-blue">sous-préfecture</strong> de votre choix (muni d’une
      pièce d’identité) pour vérifier qu’une perte de points a déjà été
      enregistrée sur le fichier national.
    </p>
    <p class="font-weight-bold">Récupération de points</p>
    <p>
      Lorsque le <strong class="c-blue">capital</strong> point devient
      <strong class="c-blue">égal ou inférieur à 0</strong>, le Ministère de
      l’intérieur envoie au titulaire du permis de conduire une
      <strong class="c-blue">lettre de notification</strong> référence 48S par
      courrier recommandé avec accusé réception. Dans ce cas,
      <span class="font-weight-bold"
        >les points relatifs à un stage sont affectés au permis de conduire dès
        lors que le premier jour du stage a eu lieu avant la date de réception
        du courrier ou avant que le délai pour retirer le courrier recommandé ne
        soit épuisé.</span
      >
    </p>
  </div>
</div>
